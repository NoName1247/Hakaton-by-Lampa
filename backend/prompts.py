def build_intent_prompt() -> str:
    return INTENT_SYSTEM


def build_plan_prompt(schema: str | None = None) -> str:
    s = schema or get_schema_context()
    return PLAN_SYSTEM.format(schema=s, synonyms=SYNONYMS)


def build_patch_prompt() -> str:
    return PATCH_SYSTEM
